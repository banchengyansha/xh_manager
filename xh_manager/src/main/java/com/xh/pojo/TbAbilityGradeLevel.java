package com.xh.pojo;

public class TbAbilityGradeLevel {
    private Integer id;

    private Integer levelDailyLiving;

    private Integer levelMentalityStatus;

    private Integer levelPerception;

    private Integer levelSocialParticipation;

    private Integer abilityLevel;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getLevelDailyLiving() {
        return levelDailyLiving;
    }

    public void setLevelDailyLiving(Integer levelDailyLiving) {
        this.levelDailyLiving = levelDailyLiving;
    }

    public Integer getLevelMentalityStatus() {
        return levelMentalityStatus;
    }

    public void setLevelMentalityStatus(Integer levelMentalityStatus) {
        this.levelMentalityStatus = levelMentalityStatus;
    }

    public Integer getLevelPerception() {
        return levelPerception;
    }

    public void setLevelPerception(Integer levelPerception) {
        this.levelPerception = levelPerception;
    }

    public Integer getLevelSocialParticipation() {
        return levelSocialParticipation;
    }

    public void setLevelSocialParticipation(Integer levelSocialParticipation) {
        this.levelSocialParticipation = levelSocialParticipation;
    }

    public Integer getAbilityLevel() {
        return abilityLevel;
    }

    public void setAbilityLevel(Integer abilityLevel) {
        this.abilityLevel = abilityLevel;
    }
}